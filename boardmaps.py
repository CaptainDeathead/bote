def value(piece, square):
    if piece == chess.KING:
        return king[str(square)]
    elif piece == chess.QUEEN:
        return queen[str(square)]
    elif piece == chess.ROOK:
        return rook[str(square)]
    elif piece == chess.BISHOP:
        return bishop[str(square)]
    elif piece == chess.KNIGHT:
        return knight[str(square)]
    elif piece == chess.PAWN:
        return pawn[str(square)]
    else:
        return 0